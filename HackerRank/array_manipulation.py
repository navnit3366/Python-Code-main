n, m = [int(i) for i in input().split()]

arr = [0 for _ in range(n)]

for i in range(m):
    a, b, k = [int(i) for i in input().split()]
    arr[a-1] += k
    try:
        arr[b] -= k
    except:
        pass

prev = 0
for i in range(len(arr)):
    prev += arr[i]
    arr[i] = prev

print(max(arr))

